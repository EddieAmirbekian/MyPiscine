int	ft_str_is_printable(char *str)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	while (1)
	{
		if (str[i] == '\0')
			break ;
		if (!(str[i] >= ' ') || str[i] == 127)
		{
			b = 0;
			break ;
		}
		i++;
	}
	return (b);
}
