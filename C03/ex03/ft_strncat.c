char	*ft_strncat(char *dest, char *src, unsigned int n)
{
	char	*s;

	s = dest;
	while (*s != '\0')
		s++;
	while (*src != '\0' && n-- > 0)
	{
		*s = *(unsigned char *)src;
		s++;
		src++;
	}
	*s = '\0';
	return (dest);
}
